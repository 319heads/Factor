				<h2><i class="fa fa-microphone"></i> Bio</h2>

				<p>Saskatoon-based super-producer Factor (née Graham Murawsky) broke into the music scene as a DJ in 1998, drawing early production inspiration from the West Coast underground and collaborations with emcees such as Kirby Dominant, along with fellow Canadian emcees Kay the Aquanaut &amp; Def3 en route to forming Side Road Records. His innovative beats are grounded in a lush, organic hip hop structure with alternating rock, folk, pop and soul sensibilities. As the years and his sound progressed, Factor’s work caught the attention of Fake Four Inc., culminating in a diverse array of projects both solo and collaborative.</p>

				<p>Since joining the Connecticut based indie label in 2008, Factor has helmed full-lengths for the likes of Myka 9, Awol One, Moka Only and Gregory Pepper while provided production for alt-rap icons such as Sole and Ceschi. Though he has diagrammed his share of filthy breaks and meticulous synths the old-fashioned way, Factor has also called in session musicians and crafted sample-free records, most notably 2011’s melodic &amp; soulful, <em>Factor and the Chandeliers</em>.</p>

				<p>The guest lists on Factor’s solo albums (e.g.<em> Lawson Graham</em>, <em>13 Stories</em>) have showcased a wide spectrum of modern underground talent, with this trend continuing on 2013’s <em>Woke Up Alone</em>, a brilliant necromancy concept record framed by the five stages of grief which features a number of the aforementioned Fake Four artists in addition to Astronautalis, Onry Ozzborn and Open Mike Eagle.</p>

				<p>Factor’s travels across the continents have seen him share stages and tracks with top tier indie hip-hop artists like Aesop Rock and Buck 65 to Xzibit and Kool Keith. There are maybe a handful of independent hip hop producers whose distinct styles are recognizable within 10 seconds of the beat dropping, and the prolific, in-demand Factor has joined the pantheon of Dilla, Madlib, El-P and Blockhead in sharing such honors.</p>
				
